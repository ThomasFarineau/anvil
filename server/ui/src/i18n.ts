const dicts: Record<Locale, Record<string, Entry>> = { en, fr };

function detect(): Locale {
  const stored = localStorage.getItem('anvil-server-locale');
  if (stored === 'en' || stored === 'fr') return stored;
  return navigator.language.toLowerCase().startsWith('fr') ? 'fr' : 'en';
}

export const [locale, setLocaleSignal] = createSignal<Locale>(detect());

export function setLocale(next: Locale) {
  localStorage.setItem('anvil-server-locale', next);
  setLocaleSignal(next);
}

export function t(key: keyof typeof en, vars: Vars = {}): string {
  const entry = dicts[locale()][key];
  return typeof entry === 'function' ? entry(vars) : entry;
}
